#include "libft.h"

void	*ft_memmove(void *dest, const void *src, size_t n)
{
	if (!src)
	{
		ft_bzero(dest, n);
		return (0);
	}
	if (src > dest)
		return (ft_strncpy(dest, src, n));
	if (src < dest)
		return (ft_strncpy_bw(dest, src, n));
	else
		return (dest);
}
